                                        <p><a href="../2013/04/Jaipur-Pink-City-Gates.jpg"><img class="size-medium wp-image-232 aligncenter" alt="Jaipur-Pink-City-Gates" src="../2013/04/Jaipur-Pink-City-Gates.jpg" /></a></p>
<p>Distance (from Delhi) &#8211; 269km (4h 50m)</p>
<p align="justify">Widely acclaimed as the Pink City of India, Jaipur proliferates in its magnificent past, bellicose history, renowned tradition, architectural elegance and natural vista. Though the Rajputs are famed for their valor and bravery, the rulers of Jaipur differ from the rest of the Rajput clan. Apart from exhibiting their chivalry, these great warriors have shown much diplomacy in their relation with the mighty kingdoms of their time. The planning of the city in accordance with the strict norms of Indian Vasthusasthra shows their credentials as good administrators. Perhaps this might be the only city in the world, which symbolizes the nine divisions of the Universe.</p>
<p align="justify">Jaipur is mostly renowned for its majestic buildings, which displays unique architectural grandeur. These stunning facades of medieval India have received a global acclaim for its intricate and exquisite charm. A quaint place, Jaipur reproduces its magnificent past through its sumptuous forts, captivating palaces, littered walls, serene temples and lovely havelis. Its effervescent panorama is attributed by the charismatic lure of its outstanding gardens. The serenity and tranquility of the verdant gardens with its marvelously tended landscapes and colorful floral arcades presents a picturesque appeal, which will certainly mesmerize any visitor.</p>
<p align="justify">It is not just monuments and palaces that Jaipur offers. Apart from an array of fascinating attractions this vivacious city holds exquisite handicrafts and marvelous jewelry. These delicate artifacts, which portray the rustic and colourful village life, would be a great treasure for every visitor.</p>
